<script lang="ts">
import { Vue, Component, Prop } from 'vue-property-decorator';

@Component
export default class VerticalSwitch extends Vue {
  @Prop({ type: Boolean, required: true }) public top!: boolean;

  public update() {
    this.$update('top', !this.top);
  }
}
</script>


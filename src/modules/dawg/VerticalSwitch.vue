<template>
  <div class="onoffswitch">
    <input 
      type="checkbox" 
      name="onoffswitch" 
      class="onoffswitch-checkbox" 
      id="myonoffswitch" 
      :value="top"
      @input="update"
    >
    <label class="onoffswitch-label" for="myonoffswitch">
        <span class="onoffswitch-inner"></span>
        <span class="onoffswitch-switch"></span>
